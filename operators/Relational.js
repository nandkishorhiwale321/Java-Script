//   relational Operator
 
console.log(10 < 3)                  // less than

console.log(10 > 3)                  // Greater than

console.log(10 <= 10)                // less than or equal to

console.log(10 >= 10)                // Greater than or equal to

console.log(10 == "10")               // equal (value only)

console.log(10 != "10")               // Not equal (value only)

console.log(20 === "20")              // Strict equal (value + type)

console.log(10 !== "10")              // Strict not equal (value + type)



